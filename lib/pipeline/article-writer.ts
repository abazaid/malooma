import type { GeneratedOutline } from "@/lib/pipeline/outline-generator";

function longParagraph(topic: string, sectionTitle: string, index: number) {
  const templates = [
    `عند الحديث عن ${topic} فإن أول ما يجب الانتباه إليه هو أن الفهم السطحي غالبًا يقود إلى قرارات سريعة لكنها غير دقيقة. لذلك من المفيد أن نبدأ بتحديد الهدف الحقيقي من هذا الموضوع: هل نبحث عن تحسين نتيجة محددة، أم نريد فهم السياق العام لاتخاذ قرار أفضل لاحقًا؟ في الواقع، توضيح الهدف منذ البداية يقلل الوقت المهدور ويجعل التنفيذ أكثر ثباتًا. كما أن تقسيم الخطوات إلى مراحل قصيرة يسهّل المتابعة اليومية ويمنع الإحباط الذي يحدث عادة عند محاولة تغيير كل شيء دفعة واحدة.`,
    `في قسم ${sectionTitle} نحتاج إلى ربط الفكرة بالنشاط اليومي، لأن المعرفة التي لا تتحول إلى ممارسة تبقى معلومة نظرية فقط. يمكن البدء بخطوة صغيرة قابلة للتنفيذ اليوم، ثم قياس أثرها بعد فترة قصيرة، ثم تحسينها تدريجيًا. هذه المنهجية التراكمية تجعل ${topic} أكثر وضوحًا وتمنحك نتائج عملية يمكن البناء عليها. ومن المهم كذلك تدوين الملاحظات أثناء التطبيق، لأن المراجعة الدورية تكشف الأنماط الخفية وتساعدك على معرفة ما يعمل فعلًا وما يحتاج إلى تعديل.`,
    `النجاح في ${topic} لا يرتبط بكمية المعلومات فقط، بل بجودة ترتيبها وطريقة استخدامها. لهذا ننصح بالانتقال من "جمع المعرفة" إلى "بناء نظام عمل" يتضمن قواعد بسيطة: تحديد الأولويات، ترتيب المهام حسب التأثير، وإزالة العوامل التي تسبب التشتيت. كلما كان النظام واضحًا، أصبح اتخاذ القرار أسرع وأكثر اتساقًا. ومع الوقت، تبدأ النتائج بالظهور بشكل متراكم، فتكتسب ثقة أعلى في خطواتك وتصبح قادرًا على تطوير الأسلوب بدل تكرار المحاولات العشوائية.`,
  ];

  return templates[index % templates.length];
}

export function writeArticleDraft(topic: string, outline: GeneratedOutline) {
  const sections = outline.h2.map((h2, index) => ({
    heading: h2,
    text: `${longParagraph(topic, h2, index)}\n\n${longParagraph(topic, h2, index + 1)}`,
  }));

  const intro = `يتعامل هذا الدليل مع موضوع ${topic} بطريقة عملية تناسب القارئ العربي الذي يبحث عن فهم واضح وخطوات قابلة للتطبيق. ستجد في هذا المقال شرحًا منظمًا، أمثلة قريبة من الواقع، ونصائح تساعدك على تجنب الأخطاء الشائعة.`;

  const bulletBlock = [
    "- ابدأ بتحديد الهدف الأساسي بدقة.",
    "- قسّم التنفيذ إلى مراحل أسبوعية.",
    "- راقب النتائج بالأرقام لا بالانطباع فقط.",
    "- عدّل الخطة بناءً على النتائج الواقعية.",
  ].join("\n");

  const conclusion = `${outline.conclusion} إذا التزمت بالمنهج المتدرج، فستلاحظ تحسنًا واضحًا في الفهم والتطبيق خلال فترة قصيرة نسبيًا.`;

  const fullText = [
    intro,
    ...sections.map((section) => `${section.heading}\n${section.text}`),
    "نقاط عملية سريعة\n" + bulletBlock,
    "الخلاصة\n" + conclusion,
  ].join("\n\n");

  const wordCount = fullText.split(/\s+/).length;

  return {
    intro,
    sections,
    bulletBlock,
    conclusion,
    wordCount,
  };
}
